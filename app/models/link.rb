class Link < ActiveRecord::Base
	
# ------------------------------------------------------------------------------
# Includes & Extensions
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Constants
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Attributes
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Associations
# ------------------------------------------------------------------------------
belongs_to :collection
belongs_to :user
has_many :favourites


# ------------------------------------------------------------------------------
# Validations
# ------------------------------------------------------------------------------

validates_presence_of :url, :description, :message => "can't be empty"
validates_format_of :url, with: URI::regexp(%w(http https)), :message => "must start with http:// or https://"

# ------------------------------------------------------------------------------
# Callbacks
# ------------------------------------------------------------------------------

before_save :sanitize_inputs


# ------------------------------------------------------------------------------
# Nested Attributes
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Scopes
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Other
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Class Methods
# ------------------------------------------------------------------------------


# ------------------------------------------------------------------------------
# Instance Methods
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
protected
# ------------------------------------------------------------------------------

def sanitize_inputs
	self.url.downcase!
	self.description.downcase!
end


# ------------------------------------------------------------------------------
private
# ------------------------------------------------------------------------------




end
