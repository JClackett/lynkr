class Link < ActiveRecord::Base
	
# ------------------------------------------------------------------------------
# Includes & Extensions
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Constants
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Attributes
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Associations
# ------------------------------------------------------------------------------
belongs_to :collection
belongs_to :user
has_many :favourites


# ------------------------------------------------------------------------------
# Validations
# ------------------------------------------------------------------------------

validates_format_of :url, with: URI::regexp(%w(http https))
validates_presence_of :url

# ------------------------------------------------------------------------------
# Callbacks
# ------------------------------------------------------------------------------

before_save :downcase_inputs


# ------------------------------------------------------------------------------
# Nested Attributes
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Scopes
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Other
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
# Class Methods
# ------------------------------------------------------------------------------


# ------------------------------------------------------------------------------
# Instance Methods
# ------------------------------------------------------------------------------



# ------------------------------------------------------------------------------
protected
# ------------------------------------------------------------------------------

def downcase_inputs
	self.url.downcase!
	self.description.downcase!
end


# ------------------------------------------------------------------------------
private
# ------------------------------------------------------------------------------




end
